# Construya un juego de buscaminas
#   - El juego debe ser de 10 x 15.
#   - Deben existir 2 etapas, uno para configurar el juego, y otro para jugar.
#   - En la primera etapa se debe solicitar cuántas bombas desea poner el el tablero. Estas no pueden superar las 10.
#   - Luego se debe colocar cada bomba en una posición solicitada por el usuario.
#   - En la segunda etapa, el jugador tiene que elegir posiciones hasta descubrir todo el tablero
#   - El jugador tiene hasta 3 vidas antes de perder.